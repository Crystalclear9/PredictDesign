class MultiAgentBenchAdapter:
    def __init__(self, context_dim: int = 16, hidden_dim: int = 32, device: str = "cpu") -> None:
        self.context_dim = context_dim
        self.hidden_dim = hidden_dim
        self.device = device

    def load_research_from_output_jsonl(self, output_jsonl: str | Path) -> list[BenchmarkEpisode]:
        output_path = Path(output_jsonl)
        if not output_path.exists():
            raise FileNotFoundError(
                f"Research output JSONL not found: {output_path}. "
                "Please run the MultiAgentBench research scenario first."
            )
        episodes: list[BenchmarkEpisode] = []
        with output_path.open("r", encoding="utf-8") as handle:
            for line_number, line in enumerate(handle, start=1):
                if not line.strip():
                    continue
                payload = json.loads(line)
                episodes.append(self._research_payload_to_episode(payload, line_number))
        return episodes

    def load_research_from_config_jsonl(self, config_jsonl: str | Path) -> list[BenchmarkEpisode]:
        raise RuntimeError(
            "Research config JSONL only contains task definitions, not executed collaboration traces. "
            "Use a discussion_output.jsonl generated by MultiAgentBench research runs."
        )

    def load_coding_from_output_jsonl(self, output_jsonl: str | Path) -> list[BenchmarkEpisode]:
        output_path = Path(output_jsonl)
        if not output_path.exists():
            raise FileNotFoundError(
                f"Coding output JSONL not found: {output_path}. "
                "Please run the MultiAgentBench coding scenario first."
            )
        task_id = self._coding_task_id_from_path(output_path)
        metadata_entry = self._load_coding_metadata_entry(task_id) if task_id is not None else None
        episodes: list[BenchmarkEpisode] = []
        with output_path.open("r", encoding="utf-8") as handle:
            for line_number, line in enumerate(handle, start=1):
                if not line.strip():
                    continue
                payload = json.loads(line)
                episodes.append(
                    self._coding_payload_to_episode(
                        payload=payload,
                        output_path=output_path,
                        line_number=line_number,
                        metadata_entry=metadata_entry,
                    )
                )
        return episodes

    def load_werewolf_from_checkpoints(self, checkpoint_dir: str | Path) -> list[BenchmarkEpisode]:
        checkpoint_dir = Path(checkpoint_dir)
        if not checkpoint_dir.exists():
            raise FileNotFoundError(
                f"Werewolf checkpoint directory not found: {checkpoint_dir}. "
                "Please run the MultiAgentBench werewolf scenario first."
            )
        checkpoint_files = self._sort_werewolf_checkpoint_paths(checkpoint_dir.glob("checkpoint_*.json"))
        if len(checkpoint_files) < 2:
            raise RuntimeError(
                f"Need at least 2 werewolf checkpoints to build temporal transitions, got {len(checkpoint_files)}."
            )
        snapshots = [json.loads(path.read_text(encoding="utf-8")) for path in checkpoint_files]
        return [self._werewolf_snapshots_to_episode(checkpoint_dir.name, snapshots)]

    def _research_payload_to_episode(self, payload: dict[str, Any], line_number: int) -> BenchmarkEpisode:
        iterations = payload.get("iterations", [])
        agent_profiles = payload.get("agent_profiles") or self._collect_research_agent_profiles(
            payload,
            iterations,
        )
        initial_nodes = [
            TemporalNode.build(
                node_id=agent_id,
                role="researcher",
                context=self._text_to_context(profile.get("profile", "")),
                context_dim=self.context_dim,
                device=self.device,
            )
            for agent_id, profile in sorted(agent_profiles.items())
        ]
        for node in initial_nodes:
            node.context_text = str(agent_profiles.get(node.node_id, {}).get("profile", "") or "")
        task_text = str(payload.get("task", ""))
        initial_structural_edges = [
            (source_node.node_id, target_node.node_id)
            for source_node in initial_nodes
            for target_node in initial_nodes
            if source_node.node_id != target_node.node_id
        ]
        query_messages = self._build_query_messages(
            query_text=task_text,
            target_node_ids=[node.node_id for node in initial_nodes],
            time_value=0.0,
        )
        steps: list[EpisodeStep] = []
        for idx, iteration in enumerate(iterations, start=1):
            messages: list[Message] = []
            for item in iteration.get("communications") or []:
                parsed = self._parse_research_communication(item)
                if parsed is None:
                    continue
                source, target, content = parsed
                messages.append(
                    self._build_completion_message(
                        source_node_id=source,
                        target_node_id=target,
                        time_value=float(idx),
                        text=content,
                    )
                )
            if idx == 1:
                messages = query_messages + messages
            observed_actions = self._message_ground_truths(messages, float(idx))
            steps.append(
                EpisodeStep(
                    observation_time=float(idx),
                    messages=messages,
                    ground_truth_action=observed_actions[0],
                    observed_actions=observed_actions,
                    context_updates=self._research_context_updates(iteration),
                    context_text_updates=self._research_context_text_updates(iteration),
                )
            )
        if not steps:
            steps.append(
                EpisodeStep(
                    observation_time=0.0,
                    messages=query_messages,
                    ground_truth_action=PredictedGraphAction(
                        action_type=GraphActionType.NO_OP,
                        score=1.0,
                        effective_time=0.0,
                    ),
                    observed_actions=[
                        PredictedGraphAction(
                            action_type=GraphActionType.NO_OP,
                            score=1.0,
                            effective_time=0.0,
                        )
                    ],
                    valid_next_actions=[
                        PredictedGraphAction(
                            action_type=GraphActionType.NO_OP,
                            score=1.0,
                            effective_time=0.0,
                        )
                    ],
                )
            )
        else:
            self._assign_future_targets(steps)
        return BenchmarkEpisode(
            episode_id=f"research_line_{line_number}",
            dataset_name="research",
            initial_nodes=initial_nodes,
            initial_edges=[],
            initial_structural_edges=initial_structural_edges,
            steps=steps,
        )

