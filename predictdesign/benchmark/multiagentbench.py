    def _werewolf_snapshots_to_episode(
        self,
        episode_id: str,
        snapshots: list[dict[str, Any]],
    ) -> BenchmarkEpisode:
        first = snapshots[0]
        players = first["shared_memory"]["private_state"]["players"]
        role_by_player = {
            player_id: player_info.get("role", "player")
            for player_id, player_info in players.items()
        }
        known_players = set(role_by_player)
        initial_nodes = [
            TemporalNode.build(
                node_id=player_id,
                role=role_by_player[player_id],
                context=self._text_to_context(player_info.get("personal_event_log", "")),
                context_dim=self.context_dim,
                device=self.device,
            )
            for player_id, player_info in sorted(players.items())
        ]
        for node in initial_nodes:
            node.context_text = str(players.get(node.node_id, {}).get("personal_event_log", "") or "")
        query_messages = self._build_query_messages(
            query_text="Werewolf game initialized.",
            target_node_ids=[node.node_id for node in initial_nodes],
            time_value=0.0,
        )
        steps: list[EpisodeStep] = []
        previous_night_events = 0
        previous_day_events = 0
        for checkpoint_index, snapshot in enumerate(snapshots, start=1):
            private_state = snapshot["shared_memory"]["private_state"]
            public_state = snapshot["shared_memory"]["public_state"]
            players = private_state.get("players", {})
            context_updates = {
                player_id: self._text_to_context(str(player_info.get("personal_event_log", "")))
                for player_id, player_info in players.items()
            }
            round_targets_history = private_state.get("werewolf_action", {}).get("round_targets", [])
            night_cache = private_state.get("night_cache") or []
            for offset, night_event in enumerate(night_cache[previous_night_events:], start=1):
                round_targets: dict[str, str] = {}
                history_index = previous_night_events + offset - 1
                if history_index < len(round_targets_history):
                    candidate = round_targets_history[history_index]
                    if isinstance(candidate, dict):
                        round_targets = {str(key): str(value) for key, value in candidate.items()}
                time_value = float(checkpoint_index) + (0.1 * offset)
                messages = self._build_werewolf_night_messages(
                    night_event=night_event,
                    players=players,
                    role_by_player=role_by_player,
                    known_players=known_players,
                    round_targets=round_targets,
                    night_index=history_index + 1,
                    time_value=time_value,
                )
                if not steps:
                    messages = query_messages + messages
                observed_actions = self._message_ground_truths(messages, time_value)
                steps.append(
                    EpisodeStep(
                        observation_time=time_value,
                        messages=messages,
                        ground_truth_action=observed_actions[0],
                        observed_actions=observed_actions,
                        context_updates=context_updates,
                        context_text_updates={
                            player_id: str((player_info.get("personal_event_log", "") or ""))
                            for player_id, player_info in players.items()
                        },
                    )
                )
            previous_night_events = len(night_cache)
            day_cache = public_state.get("day_cache") or []
            for offset, day_event in enumerate(day_cache[previous_day_events:], start=1):
                time_value = float(checkpoint_index) + 0.5 + (0.1 * offset)
                messages = self._build_werewolf_day_messages(
                    day_event=day_event,
                    sheriff=public_state.get("sheriff"),
                    known_players=known_players,
                    time_value=time_value,
                )
                if not messages:
                    continue
                if not steps:
                    messages = query_messages + messages
                observed_actions = self._message_ground_truths(messages, time_value)
                steps.append(
                    EpisodeStep(
                        observation_time=time_value,
                        messages=messages,
                        ground_truth_action=observed_actions[0],
                        observed_actions=observed_actions,
                        context_updates=context_updates,
                        context_text_updates={
                            player_id: str((player_info.get("personal_event_log", "") or ""))
                            for player_id, player_info in players.items()
                        },
                    )
                )
            previous_day_events = len(day_cache)
        if not steps:
            steps.append(
                EpisodeStep(
                    observation_time=0.0,
                    messages=query_messages,
                    ground_truth_action=PredictedGraphAction(
                        action_type=GraphActionType.NO_OP,
                        score=1.0,
                        effective_time=0.0,
                    ),
                    observed_actions=[
                        PredictedGraphAction(
                            action_type=GraphActionType.NO_OP,
                            score=1.0,
                            effective_time=0.0,
                        )
                    ],
                    valid_next_actions=[
                        PredictedGraphAction(
                            action_type=GraphActionType.NO_OP,
                            score=1.0,
                            effective_time=0.0,
                        )
                    ],
                )
            )
        else:
            self._assign_future_targets(steps)
        return BenchmarkEpisode(
            episode_id=episode_id,
            dataset_name="werewolf",
            initial_nodes=initial_nodes,
            initial_edges=[],
            initial_structural_edges=[],
            steps=steps,
        )

    def _collect_research_agent_profiles(
        self,
        payload: dict[str, Any],
        iterations: list[dict[str, Any]],
    ) -> dict[str, dict[str, str]]:
        agent_ids = self._collect_agent_ids_from_iterations(iterations)
        if not agent_ids:
            matches = re.findall(
                r"agent[_ ]?(\d+)",
                json.dumps(payload, ensure_ascii=False),
                flags=re.IGNORECASE,
            )
            agent_ids = [f"agent{match}" for match in sorted(set(matches))]
        first_assignments = {}
        if iterations:
            raw_assignments = iterations[0].get("task_assignments", {})
            if isinstance(raw_assignments, dict):
                first_assignments = {str(key): str(value) for key, value in raw_assignments.items()}
        return {
            agent_id: {"profile": first_assignments.get(agent_id, "")}
            for agent_id in sorted(agent_ids)
        }

    def _collect_coding_agent_profiles(
        self,
        iterations: list[dict[str, Any]],
        metadata_entry: dict[str, Any] | None,
    ) -> dict[str, dict[str, str]]:
        metadata_profiles: dict[str, dict[str, str]] = {}
        if isinstance(metadata_entry, dict):
            for agent in metadata_entry.get("agents", []):
                if not isinstance(agent, dict):
                    continue
                agent_id = agent.get("agent_id")
                if not agent_id:
                    continue
                metadata_profiles[str(agent_id)] = {
                    "profile": str(agent.get("profile", "") or ""),
                    "role": self._coding_role_from_agent_type(agent.get("type")),
                }
        agent_ids = sorted(set(metadata_profiles) | set(self._collect_agent_ids_from_iterations(iterations)))
        return {
            agent_id: metadata_profiles.get(
                agent_id,
                {"profile": "", "role": "developer"},
            )
            for agent_id in agent_ids
        }

    def _research_context_updates(self, iteration: dict[str, Any]) -> dict[str, list[float]]:
        updates: dict[str, list[float]] = {}
        for item in iteration.get("task_results", []):
            if not isinstance(item, dict):
                continue
            agent_id = item.get("agent_id")
            if agent_id:
                updates[str(agent_id)] = self._text_to_context(str(item.get("result", "")))
        return updates

    def _research_context_text_updates(self, iteration: dict[str, Any]) -> dict[str, str]:
        updates: dict[str, str] = {}
        for item in iteration.get("task_results", []):
            if not isinstance(item, dict):
                continue
            agent_id = item.get("agent_id")
            if agent_id:
                updates[str(agent_id)] = str(item.get("result", "") or "")
        return updates

    def _coding_context_updates(self, iteration: dict[str, Any]) -> dict[str, list[float]]:
        return {
            agent_id: self._text_to_context(result)
            for agent_id, result in self._iter_task_results(iteration)
        }

    def _coding_context_text_updates(self, iteration: dict[str, Any]) -> dict[str, str]:
        return {
            agent_id: result
            for agent_id, result in self._iter_task_results(iteration)
        }

    def _build_werewolf_night_messages(
        self,
        night_event: dict[str, Any],
        players: dict[str, Any],
        role_by_player: dict[str, str],
        known_players: set[str],
        round_targets: dict[str, str],
        night_index: int,
        time_value: float,
    ) -> list[Message]:
        messages: list[Message] = []
        for wolf_id, target_id in sorted(round_targets.items()):
            if wolf_id not in known_players or target_id not in known_players:
                continue
            messages.append(
                self._build_completion_message(
                    source_node_id=wolf_id,
                    target_node_id=target_id,
                    time_value=time_value,
                    text=f"werewolf_vote:{wolf_id}->{target_id}",
                )
            )
        if not messages:
            final_target = ((night_event.get("werewolf_action") or {}).get("final_target")) or None
            if final_target in known_players:
                for wolf_id, role in sorted(role_by_player.items()):
                    if role == "wolf" and wolf_id in known_players:
                        messages.append(
                            self._build_completion_message(
                                source_node_id=wolf_id,
                                target_node_id=str(final_target),
                                time_value=time_value,
                                text=f"werewolf_final_target:{wolf_id}->{final_target}",
                            )
                        )
        guard_player = self._find_player_by_role(role_by_player, "guard")
        guard_target = night_event.get("guard_action")
        if guard_player and guard_target in known_players:
            messages.append(
                self._build_completion_message(
                    source_node_id=guard_player,
                    target_node_id=str(guard_target),
                    time_value=time_value + 0.01,
                    text=f"guard_action:{guard_player}->{guard_target}",
                )
            )
        witch_player = self._find_player_by_role(role_by_player, "witch")
        witch_action = night_event.get("witch_action") or {}
        witch_target = witch_action.get("target")
        witch_kind = witch_action.get("action")
        if (
            witch_player
            and witch_target in known_players
            and witch_kind
            and witch_kind != "none"
        ):
            messages.append(
                self._build_completion_message(
                    source_node_id=witch_player,
                    target_node_id=str(witch_target),
                    time_value=time_value + 0.02,
                    text=f"witch_{witch_kind}:{witch_player}->{witch_target}",
                )
            )
        seer_player = self._find_player_by_role(role_by_player, "seer")
        if seer_player and seer_player in players:
            check_history = (
                ((players.get(seer_player) or {}).get("status") or {}).get("check_history") or {}
            )
            night_record = check_history.get(f"Night {night_index}") or {}
            checked_player = night_record.get("player")
            if checked_player in known_players:
                messages.append(
                    self._build_completion_message(
                        source_node_id=seer_player,
                        target_node_id=str(checked_player),
                        time_value=time_value + 0.03,
                        text=f"seer_check:{seer_player}->{checked_player}",
                    )
                )
        return messages

    def _build_werewolf_day_messages(
        self,
        day_event: dict[str, Any],
        sheriff: str | None,
        known_players: set[str],
        time_value: float,
    ) -> list[Message]:
        messages: list[Message] = []
        speech_order = day_event.get("speech_order_decision") or []
        speech_log = day_event.get("speech_log") or {}
        for speaker in speech_order:
            if speaker not in speech_log or speaker not in known_players:
                continue
            messages.append(
                self._build_completion_message(
                    source_node_id=str(speaker),
                    target_node_id=None,
                    time_value=time_value,
                    text=str(speech_log[speaker]),
                )
            )
        vote_map = day_event.get("banishment_votes") or {}
        base_offset = len(messages)
        for index, (voter, target) in enumerate(sorted(vote_map.items())):
            if voter not in known_players or target not in known_players:
                continue
            if voter == target:
                continue
            messages.append(
                self._build_completion_message(
                    source_node_id=str(voter),
                    target_node_id=str(target),
                    time_value=time_value + (0.01 * (base_offset + index + 1)),
                    text=f"banishment_vote:{voter}->{target}",
                )
            )
        return messages

    def _build_completion_message(
        self,
        source_node_id: str | None,
        target_node_id: str | None,
        time_value: float,
        text: str,
    ) -> Message:
        if source_node_id is not None and source_node_id == target_node_id:
            target_node_id = None
        message = Message.build_completion_message(
            time=time_value,
            source_node_id=source_node_id,
            target_node_id=target_node_id,
            context=self._text_to_context(text),
            hidden_dim=self.hidden_dim,
            context_dim=self.context_dim,
            device=self.device,
        )
        message.metadata["raw_text"] = text
        message.metadata["relation_type"] = self._infer_relation_type(message)
        return message

    def _build_query_messages(
        self,
        query_text: str,
        target_node_ids: list[str],
        time_value: float,
    ) -> list[Message]:
        messages: list[Message] = []
        context = self._text_to_context(query_text)
        for target_node_id in target_node_ids:
            message = Message.build_query_message(
                target_node_id=target_node_id,
                time=time_value,
                context=context,
                context_dim=self.context_dim,
                device=self.device,
            )
            message.metadata["raw_text"] = query_text
            message.metadata["query_text"] = query_text
            messages.append(message)
        return messages

    def _message_ground_truth(
        self,
        messages: list[Message],
        time_value: float,
    ) -> PredictedGraphAction:
        return self._message_ground_truths(messages, time_value)[0]

    def _assign_future_targets(self, steps: list[EpisodeStep]) -> None:
        for index, step in enumerate(steps):
            if index + 1 < len(steps):
                step.valid_next_actions = [
                    self._clone_action(action)
                    for action in steps[index + 1].observed_actions
                ]
            else:
                step.valid_next_actions = [
                    PredictedGraphAction(
                        action_type=GraphActionType.NO_OP,
                        score=1.0,
                        effective_time=step.observation_time,
                    )
                ]
            step.ground_truth_action = self._clone_action(step.valid_next_actions[0])

    def _message_ground_truths(
        self,
        messages: list[Message],
        time_value: float,
    ) -> list[PredictedGraphAction]:
        actions: list[PredictedGraphAction] = []
        seen: set[tuple[str, str | None, str | None, str | None]] = set()
        for message in messages:
            if message.source_node_id is None or message.target_node_id is None:
                continue
            if message.source_node_id == message.target_node_id:
                continue
            key = (
                GraphActionType.CREATE_EDGE.value,
                message.source_node_id,
                message.target_node_id,
                self._infer_relation_type(message),
            )
            if key in seen:
                continue
            seen.add(key)
            actions.append(
                PredictedGraphAction(
                    action_type=GraphActionType.CREATE_EDGE,
                    score=1.0,
                    effective_time=time_value,
                    source_node_id=message.source_node_id,
                    target_node_id=message.target_node_id,
                    relation_type=self._infer_relation_type(message),
                )
            )
        if actions:
            return actions
        return [
            PredictedGraphAction(
                action_type=GraphActionType.NO_OP,
                score=1.0,
                effective_time=time_value,
            )
        ]

    def _clone_action(self, action: PredictedGraphAction) -> PredictedGraphAction:
        return PredictedGraphAction(
            action_type=action.action_type,
            score=action.score,
            effective_time=action.effective_time,
            source_node_id=action.source_node_id,
            target_node_id=action.target_node_id,
            relation_type=action.relation_type,
            role=action.role,
            new_node_id=action.new_node_id,
        )

    def _infer_relation_type(self, message: Message) -> str:
        raw_text = ""
        if message.context is not None:
            raw_text = str(message.metadata.get("raw_text", "")) if message.metadata else ""
        if message.metadata and message.metadata.get("relation_type"):
            return str(message.metadata["relation_type"])
        lowered = raw_text.lower()
        if "banishment_vote:" in lowered:
            return "banishment_vote"
        if "werewolf_vote:" in lowered:
            return "werewolf_vote"
        if "werewolf_final_target:" in lowered:
            return "werewolf_attack"
        if "guard_action:" in lowered:
            return "guard_action"
        if "seer_check:" in lowered:
            return "seer_check"
        if "witch_save:" in lowered:
            return "witch_save"
        if "witch_poison:" in lowered:
            return "witch_poison"
        if "from agent" in lowered:
            return "communication"
        if "delegate" in lowered or "assign" in lowered:
            return "delegation"
        return "communication"

    def _coding_task_id_from_path(self, output_path: Path) -> int | None:
        match = re.search(r"task_(\d+)_result\.jsonl$", output_path.name)
        if match is None:
            return None
        return int(match.group(1))

    def _load_coding_metadata_entry(self, task_id: int | None) -> dict[str, Any] | None:
        if task_id is None or not CODING_METADATA_PATH.exists():
            return None
        with CODING_METADATA_PATH.open("r", encoding="utf-8") as handle:
            for line in handle:
                if not line.strip():
                    continue
                payload = json.loads(line)
                entry_task_id = payload.get("task_id")
                if entry_task_id is not None and int(entry_task_id) == task_id:
                    return payload
        return None

    def _coding_structural_edges(
        self,
        initial_nodes: list[TemporalNode],
        metadata_entry: dict[str, Any] | None,
    ) -> list[tuple[str, str]]:
        if isinstance(metadata_entry, dict):
            structural_edges: list[tuple[str, str]] = []
            for relation in metadata_entry.get("relationships", []):
                if not isinstance(relation, list) or len(relation) < 2:
                    continue
                source, target = str(relation[0]), str(relation[1])
                if source != target:
                    structural_edges.append((source, target))
            if structural_edges:
                return structural_edges
        return [
            (source_node.node_id, target_node.node_id)
            for source_node in initial_nodes
            for target_node in initial_nodes
            if source_node.node_id != target_node.node_id
        ]

    def _coding_role_from_agent_type(self, agent_type: Any) -> str:
        normalized = str(agent_type or "").strip().lower()
        if normalized == "codingagent":
            return "developer"
        return normalized or "developer"

    def _collect_agent_ids_from_iterations(self, iterations: list[dict[str, Any]]) -> list[str]:
        agent_ids: set[str] = set()
        for iteration in iterations:
            assignments = iteration.get("task_assignments", {})
            if isinstance(assignments, dict):
                agent_ids.update(str(agent_id) for agent_id in assignments)
            for item in iteration.get("task_results", []):
                if not isinstance(item, dict):
                    continue
                agent_id = item.get("agent_id")
                if agent_id:
                    agent_ids.add(str(agent_id))
        return sorted(agent_ids)

    def _iter_task_results(self, iteration: dict[str, Any]) -> Iterable[tuple[str, str]]:
        for item in iteration.get("task_results", []):
            if not isinstance(item, dict):
                continue
            agent_id = item.get("agent_id")
            if agent_id is not None:
                yield str(agent_id), str(item.get("result", "") or "")
                continue
            if len(item) == 1:
                key, value = next(iter(item.items()))
                yield str(key), str(value or "")

    def _parse_research_communication(self, item: Any) -> tuple[str | None, str | None, str] | None:
        content = str(item)
        for pattern in (
            r"From\s+agent[_ ]?(\d+)\s+to\s+agent[_ ]?(\d+)",
            r"agent[_ ]?(\d+).*?agent[_ ]?(\d+)",
        ):
            match = re.search(pattern, content, flags=re.IGNORECASE | re.DOTALL)
            if match:
                return f"agent{match.group(1)}", f"agent{match.group(2)}", content
        if not content.strip():
            return None
        return None, None, content

    def _sort_werewolf_checkpoint_paths(self, paths: Iterable[Path]) -> list[Path]:
        phase_order = {"Night": 0, "Day": 1}

        def _sort_key(path: Path) -> tuple[int, int, str]:
            match = re.fullmatch(r"checkpoint_(Night|Day)(\d+)\.json", path.name)
            if match is None:
                return (10**9, 10**9, path.name)
            phase, round_index = match.groups()
            return (int(round_index), phase_order[phase], path.name)

        return sorted((Path(path) for path in paths), key=_sort_key)

    def _find_player_by_role(self, role_by_player: dict[str, str], role_name: str) -> str | None:
        for player_id, role in sorted(role_by_player.items()):
            if role == role_name:
                return player_id
        return None

    def _text_to_context(self, text: str) -> list[float]:
        buckets = [0.0] * self.context_dim
        if not text or self.context_dim <= 0:
            return buckets

        normalized = text.lower().strip()
        tokens = re.findall(r"[a-z0-9_]+", normalized)
        compact_text = re.sub(r"\s+", " ", normalized)

        def add_feature(feature: str, weight: float) -> None:
            if not feature:
                return
            digest = hashlib.sha256(feature.encode("utf-8")).digest()
            index = int.from_bytes(digest[:8], "big") % self.context_dim
            sign = 1.0 if (digest[8] % 2 == 0) else -1.0
            buckets[index] += sign * weight

        for token in tokens:
            add_feature(f"tok:{token}", 1.0)
        for left, right in zip(tokens, tokens[1:]):
            add_feature(f"bigram:{left}|{right}", 0.75)
        for idx in range(max(len(compact_text) - 2, 0)):
            trigram = compact_text[idx : idx + 3]
            add_feature(f"tri:{trigram}", 0.25)

        for idx in range(max(len(tokens) - 2, 0)):
            trigram = "|".join(tokens[idx : idx + 3])
            add_feature(f"toktri:{trigram}", 0.6)

        keyword_weights = {
            "vote": 1.5,
            "kill": 1.5,
            "guard": 1.25,
            "witch": 1.25,
            "seer": 1.25,
            "check": 1.25,
            "banishment": 1.25,
            "query": 1.25,
            "research": 1.25,
            "discussion": 1.0,
            "summary": 1.0,
            "tool": 1.0,
            "planner": 1.0,
            "critic": 1.0,
            "solver": 1.0,
            "werewolf": 1.25,
            "villager": 1.25,
            "attack": 1.5,
            "protect": 1.25,
            "save": 1.25,
            "poison": 1.25,
            "metric": 1.0,
            "dataset": 1.0,
            "baseline": 1.0,
            "hypothesis": 1.0,
            "delegate": 1.0,
            "collaborate": 1.0,
        }
        for keyword, weight in keyword_weights.items():
            if keyword in normalized:
                add_feature(f"kw:{keyword}", weight)

        semantic_pairs = (
            ("seer", "check"),
            ("guard", "protect"),
            ("witch", "poison"),
            ("witch", "save"),
            ("werewolf", "attack"),
            ("vote", "banishment"),
            ("research", "summary"),
            ("research", "dataset"),
            ("planner", "delegate"),
        )
        for left, right in semantic_pairs:
            if left in normalized and right in normalized:
                add_feature(f"pair:{left}|{right}", 1.5)

        if self.context_dim >= 4:
            buckets[-1] = min(len(tokens) / 32.0, 1.0)
            buckets[-2] = min(len(compact_text) / 256.0, 1.0)
            buckets[-3] = compact_text.count("?") / max(len(compact_text), 1)
            buckets[-4] = compact_text.count(":") / max(len(compact_text), 1)
        if self.context_dim >= 8:
            buckets[-5] = compact_text.count("!") / max(len(compact_text), 1)
            buckets[-6] = min(len(set(tokens)) / max(len(tokens), 1), 1.0)
            buckets[-7] = min(sum(token.isdigit() for token in tokens) / max(len(tokens), 1), 1.0)
            buckets[-8] = 1.0

        norm = sum(value * value for value in buckets) ** 0.5
        if norm > 0:
            buckets = [value / norm for value in buckets]
        return buckets
